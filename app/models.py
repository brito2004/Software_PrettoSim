class Cliente:
    def __init__(self, nome_cliente, cpf, endereco, telefone, email, data_nascimento):
        self.nome_cliente = nome_cliente
        self.cpf = cpf
        self.endereco = endereco
        self.telefone = telefone
        self.email = email
        self.data_nascimento = data_nascimento

